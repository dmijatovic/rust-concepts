
pub fn variables(){
  let x:i8 = 4;
  println!("i8 {}", x);
  let y:i16 = 1024;
  println!("i16 {}", y);
  let z:i32 = 102400;
  println!("i32 {}", z);
  let q:i32 = 10240000;
  println!("i64 {}", q);
}

pub fn decimal(){
  let x:f32 = 4.002;
  println!("f32 {}", x);
  let y:f64 = 1024.00008;
  println!("f32 {}", y);
}